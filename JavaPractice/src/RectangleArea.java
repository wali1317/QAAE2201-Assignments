public class RectangleArea {
    public static void main(String[] args) {
        int length = 9;
        int breadth = 5;
        int area = length * breadth;
        int perimeter = 2 * (length * breadth);

        System.out.println("Area = " + area);
        System.out.println("Perimeter = " + perimeter);
        }
}
