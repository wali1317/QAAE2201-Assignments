public class GreaterOr {
    public static void main(String[] args) {
        int firstNumber = 23;
        int secondNumber = 32;
        int thirdNumber = 15;

        if (firstNumber>secondNumber || firstNumber>thirdNumber) {

            System.out.println("True");
        }
        else {

            System.out.println("False");
        }
    }
}
